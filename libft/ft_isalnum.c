#include "include/libft.h"

int	ft_isalnum(int c)
{
	if (c == '&' || c == ':' || c == ';' || c == '=' || c == '~' || c == '@')
		return (1);
	if ((c >= '(' && c <= '9') || c == '!' || c == '#' || c == '%')
		return (1);
	else if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
